# NumPy Basics

import numpy as np

# Creating arrays
arr = np.array([1, 2, 3, 4, 5])
print("Array:", arr)

# Array operations
print("Array + 5:", arr + 5)
print("Array * 2:", arr * 2)

# Creating zeros and ones
zeros = np.zeros(3)
ones = np.ones(3)

print("Zeros:", zeros)
print("Ones:", ones)

# 2D Array
matrix = np.array([[1, 2], [3, 4]])
print("Matrix:")
print(matrix)
